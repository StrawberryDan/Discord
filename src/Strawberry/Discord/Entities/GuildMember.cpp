namespace Strawberry::Discord::Entity
{
	Core::Result<GuildMember, Error> GuildMember::Parse(const nlohmann::json& json) noexcept
	{
		GuildMember guildMember;

		guildMember.mUser = User::Parse(json["user"]);

		return guildMember;
	}
}